     <h2>Uygun Ticaret HAKKINDA</h2>
  <p>1960 yılında Ahmet Ziylan tarafından küçük bir atölyede temelleri atılan Uygun Mağazacılık, bugün Türkiye ayakkabı pazarının tartışmasız lideri konumundadır. Doğrudan 7 binin üzerinde, dolaylı olarak 30.000 yakın kişiye istihdam sağlayan Uygun Mağazacılık, yılda 45 milyon çift ayakkabı satışı gerçekleştirmektedir.</p>
  <p>Mağazaları ile Türkiye&rsquo;nin her noktasına ulaşan Uygun Mağazacılık; Uygun markası ile yurt içinde 365&rsquo;in üzerinde, yurt dışında yakın coğrafyada ise 50&rsquo;nin üzerinde mağaza ile hizmet vermektedir. Uygun Mağazacılık bünyesinde bulunan çok markalı sneaker konseptli IN Street perakende noktaları ise Türkiye&rsquo;de 87 mağazaya ulaşmış durumdadır. Uygun Mağazacılık; Türkiye ve yurt dışında yakın coğrafyada yaklaşık 500 mağazaya sahiptir.</p>
  <p>Uygum Mağazacılık bünyesinde perakende markalarının yanı sıra, mağazalarında satışını gerçekleştirdiği ve toptan satışını yaptığı Kinetix, Polaris, Lumberjack, Butigo, Dockers by Gerli gibi her biri kendi alanında lider ayakkabı markaları bulunmaktadır.</p>
  <h2>Yurtdışında Büyüme</h2>
  <p>Uygun konseptiyle 2015 yılında yurtdışında ilk mağazasını açan Uygun Mağazacılık, şu anda Azerbaycan, Arnavutluk, İran, Suudi Arabistan, Kosova, Ürdün, Kazakistan, Irak, Gürcistan başta olmak üzere toplamda 15 ülkede 70&rsquo;in üzerinde mağazası ile hizmet vermektedir. Uygun mağazalarının dünya genelinde yaygınlaşması için çalışmalar hızla devam etmekte ve 2018 yılı sonunda 20 ülkede 100 mağazaya ulaşılması hedeflenmektedir.</p>
  <p>Avrupa&rsquo;da büyüme hedefi doğrultusunda 2012 yılında İtalyan ayakkabı markası Lumberjack&rsquo;i bünyesine katan Uygun Mağazacılık; ardından 2016 yılında Almanya&rsquo;nın en önemli yatırım fonu şirketlerinden biri olan Capiton AG ile birlikte Alman ayakkabı perakende devi Reno&rsquo;yu satın alarak Avrupa pazarında da söz sahibi olmuştur.</p>
  <p>Pakistan, Makedonya, Oman, İran, Türkmenistan ve Bulgaristan&rsquo;a bünyesinde bulundurduğu ayakkabı markaları ile ihracat yapan Uygun Mağazacılık; sahip olduğu taban fabrikasından da günde 120 bin çift taban üretmekte ve Türkiye pazarının ihtiyacını karşılamanın yanı sıra Fransa, İtalya, Portekiz, Rusya, Ukrayna, Romanya, Bulgaristan, Ürdün, Filistin, Mısır, Cezayir ve Fas gibi birçok ülkeye ihracat yapmaktadır.</p>
  <h2>Sektöre Yatırım</h2>
  <p>Uygun Mağazacılık; Türkiye&rsquo;de ayakkabı sektörünün lideri olarak, Türk ayakkabı sektörüne öncülük etme ve sektörü dünya üretim liginde üst sıralara taşıma vizyonuyla hareket etmektedir. Bu doğrultuda 2012 yılında 10 milyon TL yatırım ile  Şanlıurfa Ayakkabı Fabrikasını kuran Uygun Mağazacılık, bölgede toplam yatırım miktarı 50 milyon TL&rsquo;ye ulaşması hedeflenen bir ayakkabı üssü oluşturmayı amaçlamaktadır. Fabrikada yıllık  2 milyon 200 bin çift ayakkabı üretilmekte, yaklaşık 1000 kişiye istihdam sağlanmaktadır. Uzun vadede ise yıllık 9 milyon ayakkabı üretip yaklaşık 5 – 6 bin yeni istihdamı hedeflenmektedir.</p>
  <p>İnsana ve sürdürülebilirliğe yaptığı yatırımlarıyla da dikkat çeken Uygun Mağazacılık 2015 yılında, şu anda yıllık 132 bin ekolojik test ve 6.00 fiziksel test kapasitesine sahip olan Kalite Laboratuvarı&rsquo;nı kurdu ve Nisan 2016&rsquo;da TÜRKAK AKREDİTE belgesini aldı. Bugün Uygun mağazalarına giren her model ürün güvenliği testinden geçmektedir.</p>
  <p>2013 yılı Kasım ayında Gözde Girişim Sermayesi Yatırım ortaklığı A.Ş., BİM ve Mater Footwear BV ile güçlerini birleştirerek büyük bir ortaklığa imza atan Uygun Mağazacılık, 2023 yılında Türkiye&rsquo;den çıkacak 10 global bir markadan biri olma hedefi doğrultusunda çalışmalarına ve yatırımlarına hız kesmeden devam etmektedir.</p>
